void	ft_sort_int_tab(int *tab, int size)
{
	int	temp;
	int	i;
	int	order;

	order = 0;
	i = 0;
	while (order == 0)
	{
		order = 1;
		while (i < (size - 1))
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
				order = 0;
			}
		i++;
		}
		i = 0;
	}
}
